const lengt = 3;
const width = 5;
const heigh = 1.5;
const SurfaceArea = 2 * lengt * width + 2 * lengt * heigh + 2 * width * heigh;
console.log(`Surface Area of cuboid is:` + SurfaceArea + `square units.`);
const Volume = lengt * width * heigh;
console.log(`Volume of cuboid is:` + Volume + `cubic units.`);
